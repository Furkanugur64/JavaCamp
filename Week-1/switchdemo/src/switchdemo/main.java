package switchdemo;

public class main {

	public static void main(String[] args) {
		
		char grade='A';
		
		switch(grade) {
		
		
		case 'A':
			System.out.println("Mükemmel : İyi Geçtiniz !");
			break;
		case 'B':
			System.out.println("Mükemmel : İyi Geçtiniz !");
			break;
		case 'C':
			System.out.println("İyi :  Geçtiniz !");
			break;
		case 'D':
			System.out.println("Fena Değil :  Geçtiniz !");
			break;
		case 'F':
			System.out.println("Maalesef :  Kaldınız !");
			break;
		
		
		}

	}

}
